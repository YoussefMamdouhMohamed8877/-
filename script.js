// ==================== دالة تحديث الإحصائيات ====================
function updateStats() {
    const totalPlants = document.getElementById('totalPlants');
    const criticalPlants = document.getElementById('criticalPlants');
    const protectedPlants = document.getElementById('protectedPlants');
    
    if (totalPlants && criticalPlants && protectedPlants) {
        animateValue(totalPlants, 0, plantsData.length, 2000);
        animateValue(criticalPlants, 0, Math.floor(plantsData.length * 0.4), 2000);
        animateValue(protectedPlants, 0, Math.floor(plantsData.length * 0.25), 2000);
    }
}

function animateValue(element, start, end, duration) {
    let startTimestamp = null;
    const step = (timestamp) => {
        if (!startTimestamp) startTimestamp = timestamp;
        const progress = Math.min((timestamp - startTimestamp) / duration, 1);
        element.textContent = Math.floor(progress * (end - start) + start);
        if (progress < 1) {
            window.requestAnimationFrame(step);
        }
    };
    window.requestAnimationFrame(step);
}

