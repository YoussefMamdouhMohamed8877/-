function toggleCompare(plantId) {
    const index = selectedForCompare.indexOf(plantId);
    if (index > -1) {
        selectedForCompare.splice(index, 1);
    } else {
        if (selectedForCompare.length < 3) {
            selectedForCompare.push(plantId);
        }
    }
    updateCompareSection();
    renderPlants();
}

function updateCompareSection() {
    const compareSection = document.getElementById('compareSection');
    const compareCount = document.getElementById('compareCount');
    
    if (compareSection && compareCount) {
        if (selectedForCompare.length > 0) {
            compareSection.classList.add('active');
            compareCount.textContent = `تم اختيار ${selectedForCompare.length} نبات للمقارنة`;
        } else {
            compareSection.classList.remove('active');
        }
    }
}

