function loadQuizQuestion() {
    if (currentQuestionIndex >= quizQuestions.length) {
        currentQuestionIndex = 0;
        quizScore = 0;
    }

    const question = quizQuestions[currentQuestionIndex];
    const quizQuestion = document.getElementById('quizQuestion');
    const quizOptions = document.getElementById('quizOptions');
    const quizResult = document.getElementById('quizResult');
    
    if (!quizQuestion || !quizOptions) return;
    
    quizQuestion.textContent = question.question;
    
    const optionsHTML = question.options.map((option, index) => 
        `<div class="quiz-option" onclick="checkAnswer(${index})">${option}</div>`
    ).join('');
    
    quizOptions.innerHTML = optionsHTML;
    if (quizResult) quizResult.textContent = '';
}

function checkAnswer(selectedIndex) {
    const question = quizQuestions[currentQuestionIndex];
    const options = document.querySelectorAll('.quiz-option');
    const quizResult = document.getElementById('quizResult');
    
    if (!quizResult) return;
    
    options.forEach(option => option.style.pointerEvents = 'none');
    
    if (selectedIndex === question.correct) {
        options[selectedIndex].classList.add('correct');
        quizResult.textContent = '✅ إجابة صحيحة!';
        quizResult.style.color = '#4caf50';
        quizScore++;
    } else {
        options[selectedIndex].classList.add('wrong');
        options[question.correct].classList.add('correct');
        quizResult.textContent = '❌ إجابة خاطئة';
        quizResult.style.color = '#f44336';
    }

    quizResult.textContent += ` | النتيجة: ${quizScore}/${currentQuestionIndex + 1}`;
}

