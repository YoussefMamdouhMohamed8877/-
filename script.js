function getMostCommonThreat() {
    const threats = {};
    plantsData.forEach(plant => {
        plant.threats.forEach(threat => {
            threats[threat] = (threats[threat] || 0) + 1;
        });
    });
    
    const entries = Object.entries(threats).sort((a, b) => b[1] - a[1]);
    return entries.length > 0 ? entries[0] : null;
}

